+ (NSString *)getMachineInfo {
    size_t size;
    int nR = sysctlbyname("hw.machine", NULL, &size, NULL, 0);
    char *machine = (char *)malloc(size);
    nR = sysctlbyname("hw.machine", machine, &size, NULL, 0);
    NSString *platform = [NSString stringWithCString:machine encoding:NSUTF8StringEncoding];
    free(machine);
    return platform;
}

+ (NSString*)getDevicePlatform {
    //  系统的版本号
    NSString *systemVersion = [[UIDevice currentDevice] systemVersion];
    
    NSString *platform = [NSString stringWithFormat:@"%@ %@", [self deviceInfoWithPlatform:[self getMachineInfo]], systemVersion];
    return platform;
}

+ (NSString *)getShortDevicePlatform {
    //  系统的版本号
    NSString *systemVersion = [[UIDevice currentDevice] systemVersion];
    
    NSString *platform = [NSString stringWithFormat:@"%@ %@", [self shortDeviceInfoWithPlatform:[self getMachineInfo]], systemVersion];
    return platform;
}

